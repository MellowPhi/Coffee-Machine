package machine;

public class Coffee {
        public int productID;
        public int water;
        public int milk;
        public int coffeeBeans;
        public int cost;

        public Coffee(int id, int w, int m, int cb, int c) {
                this.productID = id;
                this.water = w;
                this.milk = m;
                this.coffeeBeans = cb;
                this.cost = c;
        }

}

